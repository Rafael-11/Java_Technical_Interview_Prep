package most_asked;

public class Merdiven {
	public static void main(String[] args) {
		// What is the Merdiven? ==> printing any object like a ledder style
		
		/*  EX: 
		   1
           12
           123
           1234
           12345
		 */
		

		for (int x = 1; x <= 10; x++) {
			for (int y = 1; y <= x; y++) {
				System.out.print(y + "");
			}
			System.out.println();
		}
		
		
	
		for(int i =1;i<=10;i++) {
			for(int j=1;j<=i; j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}
}
